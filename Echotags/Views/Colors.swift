import UIKit

extension UIColor {
    static var brandColor: UIColor {
        return UIColor(red: 0.22, green: 0.26, blue: 0.35, alpha: 1.0)
    }

    static var positiveColor: UIColor {
        return UIColor(red: 0.23, green: 0.65, blue: 0.27, alpha: 1.0)
    }
}
